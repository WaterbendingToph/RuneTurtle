turtle.setheading(0)
turtle.circle(letterHeight / 16, 180)
turtle.setheading(0)
turtle.circle(-letterHeight / 8, 90)

gapDistance = returnPoint[0] - turtle.xcor()
returnPoint = turtle.position()
turtle.setheading(60)
turtle.circle(-gapDistance, 60)

turtle.setheading(90)
turtle.circle(letterHeight / 8, 180)
turtle.setheading(180)
turtle.forward(letterHeight / 4)

turtle.penup()
turtle.goto(returnPoint)
