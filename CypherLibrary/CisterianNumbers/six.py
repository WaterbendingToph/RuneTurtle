import turtle 
window = turtle.Screen()
window.setup(width=400, height=400)
turtle.mode("logo")
turtle.penup()
turtle.goto(0, -100)
turtle.pendown()
turtle.setheading(0)
turtle.forward(150)


#START
turtle.penup()
turtle.setheading(90)
turtle.forward(50)
turtle.pendown()
turtle.setheading(180)
turtle.forward(50)
#END


turtle.done()